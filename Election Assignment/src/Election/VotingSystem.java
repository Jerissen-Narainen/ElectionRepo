    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
                String searchName = txtSearch.getText();
        
        if (searchName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Search field is empty.");
            return;
        }
        
        // Sort candidate list by last name to use binary search
        Collections.sort(candidateList, (c1, c2) -> c1.lastName.compareTo(c2.lastName));
        
        // Perform binary search
        int index = binarySearch(candidateList, searchName, 0, candidateList.size() - 1);
        
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Candidate not found.");
        } else {
            JOptionPane.showMessageDialog(this, "Candidate found: " + candidateList.get(index));
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private int binarySearch(List<Candidate> list, String searchName, int left, int right) {
        if (left > right) {
            return -1; // Not found
        }
        
        int mid = (left + right) / 2;
        Candidate midCandidate = list.get(mid);
        
        if (midCandidate.lastName.equalsIgnoreCase(searchName)) {
            return mid;
        } else if (midCandidate.lastName.compareToIgnoreCase(searchName) < 0) {
            return binarySearch(list, searchName, mid + 1, right);
        } else {
            return binarySearch(list, searchName, left, mid - 1);
        }
    }
    
