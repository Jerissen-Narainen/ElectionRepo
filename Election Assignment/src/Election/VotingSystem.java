    private void btnVoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoteActionPerformed
     String enteredNIC = JOptionPane.showInputDialog(this, "Please enter your NIC:");

        if (enteredNIC == null || enteredNIC.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIC cannot be empty.");
            return;
        }

        // Validate NIC
        boolean isValidVoter = validateVoterNIC(enteredNIC);
        if (!isValidVoter) {
            JOptionPane.showMessageDialog(this, "Invalid NIC. You must be a registered voter to vote.");
            return;
        }

       
        if (votersWhoHaveVoted.contains(enteredNIC)) {
            JOptionPane.showMessageDialog(this, "You have already voted. You cannot vote twice.");
            return;
        }

        List<String> selectedCandidates = new ArrayList<>();

        for (JCheckBox checkbox : checkboxes) {
            if (checkbox.isSelected()) {
                selectedCandidates.add(checkbox.getText());
            }
        }

        if (selectedCandidates.size() > 3) {
            JOptionPane.showMessageDialog(this, "You can only vote for up to 3 candidates.");
        } else if (selectedCandidates.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select at least one candidate.");
        } else {
            // Update vote counts
            for (String candidateName : selectedCandidates) {
                voteCounts.put(candidateName, voteCounts.getOrDefault(candidateName, 0) + 1);
            }
            
            // Mark this NIC as having voted
            votersWhoHaveVoted.add(enteredNIC);

            JOptionPane.showMessageDialog(this, "Your vote has been recorded.");
        }
    }//GEN-LAST:event_btnVoteActionPerformed


       
    
    private boolean validateVoterNIC(String nic) {
    // Retrieve the table model from voterTable2
    DefaultTableModel model = (DefaultTableModel) voterTable2.getModel();

    // Iterate through each row of the table
    for (int i = 0; i < model.getRowCount(); i++) {
        String tableNIC = (String) model.getValueAt(i, 0); // Assuming NIC is in the first column

        // Check if the entered NIC matches the NIC in the table
        if (nic.equals(tableNIC)) {
            return true; // NIC found in the table
        }
        
    }

    return false; // NIC not found in the table
    }
    
    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
   String lastName = txtlname.getText();
    String firstName = txtfname.getText();
    
    if (lastName.isEmpty() || firstName.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in both fields.");
        return;
    }
    
    // Create new candidate and add to the list
    Candidate candidate = new Candidate(lastName, firstName);
    candidateList.add(candidate);

    // Debugging statements
    System.out.println("Candidate added: " + candidate.getFirstName() + " " + candidate.getLastName());

    populateCheckBoxes();
    updateCandidateTable();
    }//GEN-LAST:event_btnRegisterActionPerformed
   
private void updateCandidateTable() {
    // Assuming you have a DefaultTableModel for candidateTable1
    DefaultTableModel model = (DefaultTableModel) candidateTable1.getModel();
    model.setRowCount(0); // Clear existing rows
    
    for (Candidate candidate : candidateList) {
        // Add new rows to the table
        model.addRow(new Object[]{candidate.getFirstName(), candidate.getLastName()});
        
    }
}

    
