    private String[] binarySearch2(List<String[]> sortedList2, String targetNIC, int left, int right) {
    if (left > right) {
        return null; // Base case: target not found
    }
    
    int mid = left + (right - left) / 2;
    String[] midValue = sortedList2.get(mid);
    
    if (midValue[0].equals(targetNIC)) {
        return midValue; // Found the target NIC
    }
    
    if (targetNIC.compareTo(midValue[0]) < 0) {
        return binarySearch2(sortedList2, targetNIC, left, mid - 1); // Search in the left half
    } else {
        return binarySearch2(sortedList2, targetNIC, mid + 1, right); // Search in the right half
    }
}
    
