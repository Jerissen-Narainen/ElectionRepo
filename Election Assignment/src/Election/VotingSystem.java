    private void updateResultTable() {
    // Retrieve the table model from resultTable1
    DefaultTableModel model = (DefaultTableModel) resultTable1.getModel();

    // Clear existing rows
    model.setRowCount(0);

    // Add rows for each candidate and their vote counts
    for (Map.Entry<String, Integer> entry : voteCounts.entrySet()) {
        String[] nameParts = entry.getKey().split(" ", 2);
        String firstName = nameParts[0];
        String lastName = nameParts.length > 1 ? nameParts[1] : "";

        model.addRow(new Object[]{firstName, lastName, entry.getValue()});
    }
}
    
